// getting data using object destructuring
const { buildSchema } = require("graphql");

// pass props in schema
// String! means type is string but can't be null
// every event must have an ID
// ! means not nullable

// createEvent(...): Event == means when you call create event you must return an event
//  password should be nullable in type as we don't want to send it to any user

module.exports = buildSchema(`

type Booking{
    _id: ID!
    event: Event!
    user: User!
    createdAt: String!
    updatedAt: String!
}

type Event{
    _id: ID!
    title: String!
    description: String!
    price: Float!
    date: String!
    creator: User!
}

type User{
    _id:ID!
    email: String!
    password: String
    createdEvents: [Event!]
}

input EventInput{
    title: String!
    description: String!
    price: Float!
    date: String!
}

input UserInput{
    email: String!
    password: String!
}

type RootQuery{
    events: [Event!]!
    bookings:[Booking!]!
}

type RootMutation{
    createEvent(eventInput: EventInput): Event
    createUser(userInput: UserInput) : User
    bookEvent(eventId: ID!) : Booking!
    cancelBooking(bookingId: ID!) : Event!
}

schema{
    query: RootQuery
    mutation: RootMutation
}
`);
